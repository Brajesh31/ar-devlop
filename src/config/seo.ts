export const getEventSchema = (event: {
  name: string;
  description: string;
  startDate: string;
  endDate?: string;
  location?: string;
  image?: string;
  url: string;
}) => ({
  '@context': 'https://schema.org',
  '@type': 'Event',
  name: event.name,
  description: event.description,
  startDate: event.startDate,
  endDate: event.endDate || event.startDate,
  eventStatus: 'https://schema.org/EventScheduled',
  eventAttendanceMode: 'https://schema.org/MixedEventAttendanceMode',
  location: event.location ? {
    '@type': 'Place',
    name: event.location,
    address: {
      '@type': 'PostalAddress',
      addressCountry: 'IN',
    },
  } : {
    '@type': 'VirtualLocation',
    url: event.url,
  },
  image: event.image || 'https://bharatxr.co/og-image.png',
  organizer: {
    '@type': 'Organization',
    name: 'Bharat XR',
    url: 'https://bharatxr.co',
  },
});
